package com.hanbit.team03.core.vo;

public class CinemaFileVO {

	private String CinemaFileid;
	private String CinemaFileSize;
	private String CinemaFilePath;
	private String CinemaContentType;
	private String CinemaFileName;
	private byte[] CinemaFileData;
	public String getCinemaFileid() {
		return CinemaFileid;
	}
	public void setCinemaFileid(String cinemaFileid) {
		CinemaFileid = cinemaFileid;
	}
	public String getCinemaFileSize() {
		return CinemaFileSize;
	}
	public void setCinemaFileSize(String cinemaFileSize) {
		CinemaFileSize = cinemaFileSize;
	}
	public String getCinemaFilePath() {
		return CinemaFilePath;
	}
	public void setCinemaFilePath(String cinemaFilePath) {
		CinemaFilePath = cinemaFilePath;
	}
	public String getCinemaContentType() {
		return CinemaContentType;
	}
	public void setCinemaContentType(String cinemaContentType) {
		CinemaContentType = cinemaContentType;
	}
	public String getCinemaFileName() {
		return CinemaFileName;
	}
	public void setCinemaFileName(String cinemaFileName) {
		CinemaFileName = cinemaFileName;
	}
	public byte[] getCinemaFileData() {
		return CinemaFileData;
	}
	public void setCinemaFileData(byte[] cinemaFileData) {
		CinemaFileData = cinemaFileData;
	}



}
