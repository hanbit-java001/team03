package com.hanbit.team03.core.vo;

public class MovieFilesVO {

	private String Movie_Fileid;
	private String Movie_FileSize;
	private String Movie_FilePath;


	public String getMovie_Fileid() {
		return Movie_Fileid;
	}
	public void setMovie_Fileid(String movie_Fileid) {
		Movie_Fileid = movie_Fileid;
	}
	public String getMovie_FileSize() {
		return Movie_FileSize;
	}
	public void setMovie_FileSize(String movie_FileSize) {
		Movie_FileSize = movie_FileSize;
	}
	public String getMovie_FilePath() {
		return Movie_FilePath;
	}
	public void setMovie_FilePath(String movie_FilePath) {
		Movie_FilePath = movie_FilePath;
	}


}
