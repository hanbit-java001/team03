package com.hanbit.team03.core.vo;

public class SweetShopVO {

   private int sweetShopId;
   private String sweetShopProduct;
   private int sweetShopPrice;
   private String sweetShopPath;
   private String sweetShopComponent;
   private String sweetShopUsingLimit;

   public int getSweetShopId() {
      return sweetShopId;
   }
   public void setSweetShopId(int sweetShopId) {
      this.sweetShopId = sweetShopId;
   }
   public String getSweetShopProduct() {
      return sweetShopProduct;
   }
   public void setSweetShopProduct(String sweetShopProduct) {
      this.sweetShopProduct = sweetShopProduct;
   }
   public int getSweetShopPrice() {
      return sweetShopPrice;
   }
   public void setSweetShopPrice(int sweetShopPrice) {
      this.sweetShopPrice = sweetShopPrice;
   }
   public String getSweetShopPath() {
      return sweetShopPath;
   }
   public void setSweetShopPath(String sweetShopPath) {
      this.sweetShopPath = sweetShopPath;
   }
   public String getSweetShopComponent() {
      return sweetShopComponent;
   }
   public void setSweetShopComponent(String sweetShopComponent) {
      this.sweetShopComponent = sweetShopComponent;
   }
   public String getSweetShopUsingLimit() {
      return sweetShopUsingLimit;
   }
   public void setSweetShopUsingLimit(String sweetShopUsingLimit) {
      this.sweetShopUsingLimit = sweetShopUsingLimit;
   }



}